// opcodePrice returns the deployment prices of specified opcodes
func opcodePrice(opcodes ...opcode.Opcode) util.Fixed8 {
	var result util.Fixed8
	for _, op := range opcodes {
		result += util.Fixed8(prices[op])
	}
	return result
}

